# agents/case_context_agent.py

class CaseContextAgent:
    def generate_case_study(self):
        # Example case study and questions
        context = "You are a consultant hired by a retail company to improve their online customer engagement."
        questions = [
            "What are the key factors the company should consider?",
            "How can the company improve its online engagement?",
            "What would you recommend to increase customer loyalty?"
        ]
        return context, questions
